    public function filterOrder(Request $request)
    {
//        dd($request->all());
        $orders = Order::whereHas('restaurant', function ($query) use ($request) {
            $query->where('restaurant_id', $request->restaurant_id)->orWhere('status','!==','delivered');
        })->get();
        return view('orders.index', compact('orders'));
    }

    public function changeStatus($order)
    {
        $order = Order::find($order);

        if ($order->status == ('pending')) {
            $order->status = ('preparing');
        } elseif ($order->status == ('preparing')) {
            $order->status = ('shipping');
        } elseif ($order->status == ('shipping')) {
            $order->status = ('delivered');
        }
        $order->save();

        return redirect()->route('orders.index')->with('message', 'Status Changed');
    }
